    private boolean updateLatestDataIntoDb(){
        //to convert default date format into yyyy-MM-dd format
         //note month must be MM not mm
        
        SimpleDateFormat dateFormat1= new SimpleDateFormat("yyyy-MM-dd");
        
        String date2=dateFormat1.format(Date1.getDate());

        

        // 1
        String query ="UPDATE fees "
                + "SET reciept_no=?,check_no=?,bank_name=?,date=?,recieved_from=?,roll_no=?,course=?,total_amount=?,remark=?,payment_mode=?,dd_no=?,start_year=?,end_year=?,amount=?,sgst=?,gst=?,Ttl_in_words=?"
                + "WHERE reciept_no='"+Integer.parseInt(recieptTxt.getText())+"'"; //(SELECT MAX(reciept_no))"; don't use this
        fmsConnection fc = new fmsConnection(query);
          
            
        try{
            
            fc.pStmt.setString(1,recieptTxt.getText());
            fc.pStmt.setString(2,checkTxt.getText());
            fc.pStmt.setString(3,bankNameTxt.getText());
          
            fc.pStmt.setString(4,date2);
           
            fc.pStmt.setString(5,recievedFromTxt.getText());
            fc.pStmt.setInt(6, Integer.parseInt(rollNoTxt.getText()));
          
            fc.pStmt.setString(7, courseTxt.getText());
            fc.pStmt.setInt(8, (int)Float.parseFloat(totalAmountTxt.getText()));
            fc.pStmt.setString(9, remarkTxt.getText());
            fc.pStmt.setString(10, paymentOptionCombo.getSelectedItem().toString());
            fc.pStmt.setString(11,ddTxt.getText() );
            
            fc.pStmt.setInt(12,startYearTxt.getYear() );
            fc.pStmt.setInt(13,endYearTxt.getYear() );
            fc.pStmt.setInt(14,Integer.parseInt(amountTxt.getText()));
            fc.pStmt.setString(15,sGstTxt.getText() );
            fc.pStmt.setString(16,gstTxt.getText() );
            fc.pStmt.setString(17,totalInWordsTxt.getText());
            
           

            fc.pStmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Update Successfull...");
            fc.con.close();
            return true;
        }
        catch(Exception e){
            System.out.println("Update failed.");
            e.printStackTrace();
             return false;
        }
        
    }
    
