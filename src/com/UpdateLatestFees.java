    public boolean checkEmptyField(){//check for empty fields..............................
        if(recieptNo.equals("")){
            JOptionPane.showMessageDialog(this, "please Enter Reciept no.!");
        return false;
        }
        if(date==null){
            JOptionPane.showMessageDialog(this, "please Select Date!");
            System.out.println("date..................."+date);
        return false;
        }
        if(paymentOptionCombo.getSelectedItem().equals("Check")){
            if( check_no.equals("")){
                JOptionPane.showMessageDialog(this, "please Enter check no.!");
                 return false;
            }
            if( bankName.equals("")){
                JOptionPane.showMessageDialog(this, "please Enter Bank Name!");
                 return false;
            }
        }
        if(paymentOptionCombo.getSelectedItem().equals("DD")){
            if( dd_no.equals("")){
                JOptionPane.showMessageDialog(this, "please Enter DD no.!");
                 return false;
            }
            if( bankName.equals("")){
                JOptionPane.showMessageDialog(this, "please Enter Bank Name!");
                 return false;
            }
        }
        if(paymentOptionCombo.getSelectedItem().equals("Card")){
            
            if( bankName.equals("")){
                JOptionPane.showMessageDialog(this, "please Enter Bank Name!");
                 return false;
            }
        }
        if(recieved_from.equals("")){
            JOptionPane.showMessageDialog(this, "please Enter Recieved from.!");
        return false;
        }
        
        if(roll_no.equals("")){
            JOptionPane.showMessageDialog(this, "please Enter Roll no.!");
        return false;
        }
        
        if(amount.equals("")){
            JOptionPane.showMessageDialog(this, "please Enter Amount!");
        return false;
        }
        
     return true;
    }
    
   
    
