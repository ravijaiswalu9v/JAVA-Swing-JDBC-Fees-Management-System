    private void themeToggleButtonItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_themeToggleButtonItemStateChanged
        int state = evt.getStateChange();

        // if selectDark print dark theme in console
        if (state == java.awt.event.ItemEvent.SELECTED) {
            selectDark();
            Theme.setTheme("dark");
        }
        else { // else  print light theme theme in console
            selectLight();
            Theme.setTheme("light");
        }
    }//GEN-LAST:event_themeToggleButtonItemStateChanged

