    private void searchFees(){
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
       
        String fromDate= dateFormat.format(staartDate.getDate());
        String toDate = dateFormat.format(endDate.getDate());
        String course1 = courseCombo.getSelectedItem().toString();
        
     String query = "SELECT * FROM fees WHERE date BETWEEN '"+fromDate+"' AND '"+toDate+"' AND course='"+course1+"'";
     fmsConnection fc = new fmsConnection(query);
     try{
      ResultSet rs = fc.pStmt.executeQuery();
      double totalAmount=0.0;
      while(rs.next()){
//roll_no | first_name | last_name | father_name|mother_name|DOB|courseID|contact_no|email| address| country
          String rollNo     = rs.getString("roll_no");
          String recieptNO  = rs.getString("reciept_no");
          String studentName   = rs.getString("recieved_from");
          String courseName      = rs.getString("course");
          String amount      = rs.getString("amount");
          String remark     = rs.getString("remark");
          String date1     = rs.getDate("date").toString();
          
//        String recieptNo = rs.getString("");
          Object obj[] = {recieptNO,rollNo,studentName,courseName,amount,remark,date1};
          model=(DefaultTableModel)tableFees.getModel();
          model.addRow(obj);
          
          totalAmount = totalAmount+Double.parseDouble(amount);
     }
      courseSlbl.setText(course1);
      amountLbl.setText(String.valueOf(totalAmount));
      //converting netAmount into integer and then pass value to convert
       String word= NumberToWordsConverter.convert((int)totalAmount);
       totalAmntInWordLbl.setText(word+" only.");
    }
    catch(Exception e){
        e.printStackTrace();
    }
    }
    
