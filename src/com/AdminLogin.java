    private boolean checkEmptyFields(){
        if(uNameTxt.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Username can't be empty..");
            return false;
        }
        if(pwdTxt.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Password can't be empty..");
            return false;
        }
    return true;
    }
    
