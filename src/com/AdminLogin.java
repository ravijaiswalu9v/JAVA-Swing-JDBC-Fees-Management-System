    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        // TODO add your handling code here:

        
        System.out.println("Login button clicked.");
        
        if(myValidate()){
           
            String uname=uNameTxt.getText();
            String pwd = pwdTxt.getText();
//             
            String query ="SELECT * FROM admin WHERE user_name=? and password=?"; 
            fmsConnection fc = new fmsConnection(query);
            try {
                fc.pStmt.setString(1, uname);
                fc.pStmt.setString(2, pwd);
                ResultSet rs= fc.pStmt.executeQuery();
                if(rs.next()){
                    System.out.println("test.");
                    JOptionPane.showMessageDialog(this, "Login SuccessFull..");
                    AdminDashboard home = new AdminDashboard();
                    home.setVisible(true);
                    this.dispose();
                }
                else{
                JOptionPane.showMessageDialog(this, "Please Enter Correct Username and Password..");
                }
            } catch (SQLException ex) {
                Logger.getLogger(AdminLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_loginBtnActionPerformed

