     private void searchCourse(){
     String query = "SELECT * FROM course";
     fmsConnection fc = new fmsConnection(query);
     try{
      ResultSet rs = fc.pStmt.executeQuery();
      while(rs.next()){
          String id = rs.getString("courseId");
          String name = rs.getString("courseName");
          String fees = rs.getString("courseFees");
          String duration = rs.getString("courseDuration");
//        String recieptNo = rs.getString("");
          Object obj[] = {id, name, fees, duration};
          model=(DefaultTableModel)tableCourse.getModel();
          model.addRow(obj);
     }
    }
    catch(Exception e){
        e.printStackTrace();
    }
    }
    
    
