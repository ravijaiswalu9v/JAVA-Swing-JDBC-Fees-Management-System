    private void searchStudents(){
     String query = "SELECT * FROM student";
     fmsConnection fc = new fmsConnection(query);
     try{
      ResultSet rs = fc.pStmt.executeQuery();
      while(rs.next()){
//roll_no | first_name | last_name | father_name|mother_name|DOB|courseID|contact_no|email| address| country
          String RollNo     = rs.getString("roll_no");
          String CourseID   = rs.getString("courseID");
          String FName      = rs.getString("first_name");
          String LName      = rs.getString("last_name");
          String DOB        = rs.getString("DOB");
          String FatherName = rs.getString("father_name");
          String MotherName = rs.getString("mother_name");
          String ContactNo  = rs.getString("contact_no");
          String Email      = rs.getString("email");
          String Country    = rs.getString("country");
          String Address    = rs.getString("address");
          
//        String recieptNo = rs.getString("");
          Object obj[] = {RollNo,CourseID,FName,LName,DOB,FatherName,MotherName,ContactNo,Email,Country,Address};
          model=(DefaultTableModel)tableStudent.getModel();
          model.addRow(obj);
     }
    }
    catch(Exception e){
        e.printStackTrace();
    }
    }
    
